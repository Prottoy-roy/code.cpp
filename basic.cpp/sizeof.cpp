#include<iostream>
using namespace std;
int main() {
  int age;
  double age1;
  float age2;
  char age3;
  bool age4;
  cout << "int = " << sizeof(age) << endl;
  cout << "double = " << sizeof(age1) << endl;
  cout << "float = " << sizeof(age2) << endl;
  cout << "char = " << sizeof(age3) << endl;
  cout << "bool = " << sizeof(age4) << endl;
  return 0;
}